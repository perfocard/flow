<?php

namespace Perfocard\Flow\Console\Commands;

use Illuminate\Foundation\Console\EnumMakeCommand as BaseEnumMakeCommand;

class EnumMakeCommand extends BaseEnumMakeCommand
{
    /**
     * The console command name.
     *
     * @var string
     */
    protected $name = 'make:enum';

    /**
     * The console command description.
     *
     * @var string
     */
    protected $description = 'Create a new enum for model';

    /**
     * Replace the class name for the given stub.
     *
     * @param  string  $stub
     * @param  string  $name
     * @return string
     */
    protected function replaceClass($stub, $name)
    {
        $stub = parent::replaceClass($stub, $name);

        return str_replace('class', $this->argument('name'), $stub);
    }

    protected function getStub()
    {

        return base_path('stubs/enum.stub');
    }

    /**
     * Get the default namespace for the class.
     *
     * @param  string  $rootNamespace
     * @return string
     */
    protected function getDefaultNamespace($rootNamespace)
    {
        return $rootNamespace.'\Models';
    }
}
